</p><h2><a name="SECTION0003300000000000000000">
2.3 Características</a>
</h2>

<p>
Las características del personaje se componen de los puntos de vida,
la energía, el estrés y los stats. La vida evidentemente mide el daño
que podemos sufrir antes de morir<a name="tex2html7" href="#foot110"><sup>7</sup></a>, la energía nos da una idea de cuantas cosas podemos hacer antes
de agotarnos, el estrés mide lo estresados o preocupados que estamos
y el alineamiento nos dice si abrazamos la luz o la oscuridad. Los
stats son el perfil de nuestro personaje pasado a números.

</p><p>
</p><dl>
<a name="vida"></a><dt><strong>Puntos de Vida</strong></dt>
<dd>Representan la cantidad de daño que podemos recibir
antes de quedar inconscientes. Todos empezamos con 100. Se puede entrenar
para aumentar la vida máxima.
</dd>
<a name="energia"></a><dt><strong>Energia</strong></dt>
<dd>Es la capacidad de trabajo que tenemos. Cada acción consume
energía, si llegamos a cero simplemente no podemos hacer ninguna otra
acción. La energía se regenera automáticamente cada hora<a name="tex2html8" href="#foot112"><sup>8</sup></a>. Todos empezamos con 200 y cuando ascendemos de nivel nuestra energía
máxima también aumenta. También se puede entrenar para aumentar la
energia máxima.
</dd>
<a name="estres"></a><dt><strong>Estrés</strong></dt>
<dd>Aunque sea redundante mide nuestro estrés, o, visto de otra
forma, lo preocupados o cansados mentalmente que estamos. Su efecto
se refleja en nuestras acciones de forma que con un estres muy alto
realizamos mal cualquier acción. Si nuestro estres alcanza el máximo
moriremos<a name="tex2html9" href="#foot113"><sup>9</sup></a>. El estrés es una cantidad fija y no se puede entrenar ni aumenta
al subir de nivel. Todos tenemos un máximo de estrés de 1000.
</dd>
<a name="lado"></a><dt><strong>Alineación</strong></dt>
<dd>Nos dice a que lado de la fuerza estamos. Los puntos
positivos son puntos de la luz, si seguimos por este camino seremos
Jedi. Los puntos negativos nos encaminan al lado oscuro, si tomamos
esta decisión acabaremos convirtiendonos en Sith. La forma de ganar
(o perder) estos puntos es bien sencilla, si hacemos trabajos nos
encaminaremos hacia la luz, por el contrario si cometemos crimenes
tornaremos nuestro camino hacia el reverso tenebroso. El límite están
en 200 puntos, tanto para la luz como para la oscuridad.
</dd>
<a name="vigor"></a><dt><strong>Vigor</strong></dt>
<dd>Mide nuestra fuerza física. Marca el daño que hacemos con
nuestros ataques físicos. Es entrnable.
</dd>
<a name="constitucion"></a><dt><strong>Constitución</strong></dt>
<dd>Mide nuestra forma física y nuestra resistencia. Es
muy importante a la hora de recibir golpes ya que cuanto más alta
mejor aguantaremos el daño físico. Es entrenable.
</dd>
<a name="destreza"></a><dt><strong>Destreza</strong></dt>
<dd>Mide nuestra habilidad. Cuanto más alta, mas precisos y
rápidos serán nuestros golpes. Es entrenable.
</dd>
<a name="inteligencia"></a><dt><strong>Inteligencia</strong></dt>
<dd>Marca tu desarrollo intelectual y tu raciocinio. Va
a afectar a el uso de la fuerza y a tus capacidades como cazador.
Es entrenable.
</dd>
<a name="poder"></a><dt><strong>Poder de la Fuerza</strong></dt>
<dd>Mide nuestra comunión con la fuerza o visto
de otra manera cuan potentemente la usamos. Es entrenable.
</dd>
</dl>
En cada nivel tenemos un límite a las carácteristicas del personaje.
Hemos decidido no ponerlas ya que nos parece mejor que las vayais
descubriendo. Eso sí que a medida que subamos de nivel el máximo posible
de las características aumenta.

<p>

<a name="nivel"></a></p><h2><a name="SECTION0003400000000000000000">
2.4 Nivel del personaje</a>
</h2>

<p>
Como habitantes con capacidades de sentir la fuerza entramos en una
sociedad que estamentiza lo que somos. Un jugador que aun no ha empezado
su entrenamiento tiene menos capacidades que un Caballero o un Guerrero,
por ejemplo. Una aclaración antes de nada, podemos ser o Jedi o Sith<a name="tex2html10" href="#foot116"><sup>10</sup></a>, la que nos va a marcar como tal son nuestras acciones que a su vez
marcan nuestra inclinación en la fuerza (luz u oscuridad). Vamos a
ver que niveles hay:

</p><p>
</p><dl>
<dt><strong>Usuario de la Fuerza</strong></dt>
<dd>Es común para todos y es donde empezamos.
Somos unos seres con sensibilidad superior a los comunes a sentir
la fuerza, pero ni sabemos como manipularla ni la sentimos plenamente.
En este nivel no podremos atacar ni ser atacados, sin embargo podemos
trabajar, robar y cazar y por supuesto entrenar. Para pasar al siguiente
nivel hemos de entrar en una academia, para ello deberemos pagar 10.000
créditos.
</dd>
<dt><strong>Padawan/Aprendiz</strong></dt>
<dd>En este nivel nos hemos iniciado en los caminos
de la fuerza. Ya tenemos nociones básicas de como usarla y además
tendremos nuestro sable de luz. Ahora empieza el juego de verdad,
en este nivel podemos atacar y ser atacados. Además de seguir pudiendo
trabajar, robar, cazar y entrenar. Para subir de nivel tenemos que
ganar 10 combates.
</dd>
<dt><strong>Caballero/Guerrero</strong></dt>
<dd>Nuestro conocimiento de la fuerza está avanzado,
adquirimos nuevos poderes y los combates serán más encarnizados que
nunca. Seguimos teniendo las mismas formas de trabajo y de entrenamiento,
pero la gran novedad es que podremos fundar nuestro propio clan y
comenzar a ser importantes colaboradores (o no) de la estabilidad
de los planetas y cuidades del universo. Para subir al máximo nivel
hay que acumular 1000 puntos de merito.
</dd>
<dt><strong>Maestro/Lord</strong></dt>
<dd>No hay nadie más poderoso que los Maestros y lo Lores.
Nuestros conocimientos de la fuerza son tan profundos como ella misma
y pasamos a ser un todo con ella. Los combates son a unos niveles
inimaginables para los simples habitantes de la galaxia, hemos desarrollado
plenamente nuestros poderes y los usamos con sabiduria y eficacia.
Es aquí donde llegaremos a los máximos alcanzables por cualquier raza.
No se puede subir a partir de este nivel.
</dd>
</dl>

<p>

</p><h2><a name="SECTION0003500000000000000000">
2.5 Dinero</a>
</h2>

<p>
Esta claro que todos queremos ser lo mejor de lo mejor. Para eso además
de ser un alumno aplicado hay que tener dinero y para eso hay que
trabajar<a name="tex2html11" href="#foot120"><sup>11</sup></a>. Entendemos trabajar a cualquier forma de hacer dinero que hay en
el juego: Trabajos, Crimenes, Cazar o Colaborar con la Ciudad.

</p><p>

</p><h3><a name="SECTION0003510000000000000000">
2.5.1 Trabajo</a>
</h3>

<p>
Hay varios trabajos: Mercader, Tendero, Reponedor, Artesano y Portero.
El primer trabajo es para todo el mundo igual, el resto dependen de
nuestro stat en inteligencia, vigor, destreza y constitución respentivamente.
Trabajar de este modo nos reporta un beneficio variable y ganaremos
puntos de luz (5 puntos). Los trabajos gastan energia, 5 puntos cada
vez que trabajas. Puedes trabajar las veces que quieras hasta que
la energía se te agote o no sea suficiente para hacer un trabajo.

</p><p>

</p><h3><a name="SECTION0003520000000000000000">
2.5.2 Crimen</a>
</h3>

<p>
Igual que en los trabajos, hay varios: Robar en Desguace, Pedir, Romper
Máquina, Vaciar Bolsillos y Amenazar. El primero es igual para todo
el mundo mientras que el resto dependen del nivel que tengas en los
distintos stats (inteligencia, vigor, destreza y constitución respentivamente).
Al cometer un crimen ganaremos creditos de forma variable y obtendremos
7 puntos de oscuridad. Para cometer un crimen hay que gastar 5 puntos
de energía. Al igual que en el trabajo podrás robar hasta que no te
quede energia o no sea suficiente para completar una acción de robo.
De todas formas el crimen como tal está perseguido y en la mayoría
de las ciudades la policía está muy atenta, si nos coge robando entonces
nos multará<a name="tex2html12" href="#foot123"><sup>12</sup></a>.

</p><p>

</p><h3><a name="SECTION0003530000000000000000">
2.5.3 Cazar</a>
</h3>

<p>
La caza es un trabajo un poco distinto. Se gasta más energía, 7 puntos
por cada vez que salimos a cazar, y entablaremos un combate con el
animal que pretendemos cazar. Muchas veces saldremos magullados de
nuestras cazas, y tendremos que ir a curarnos. La mecánica de la caza
es simle, lo único que hay que hacer es escoger al animal que deseamos
cazar<a name="tex2html13" href="#foot125"><sup>13</sup></a> y allá vamos. Si lo cazamos obtendremos un dinero por el animal,
si no habremos perdido la energía gastada en ir a cazar y seguramente
algún que otro punto de vida.

</p><p>

</p><h3><a name="SECTION0003540000000000000000">
2.5.4 Colaborar con la Ciudad</a>
</h3>

<p>
Hay otra opción de ganar dinero, depende de la ciudad. En cada ciudad
puede haber minas y astilleros. En estos se nos pagará una cantidad
por punto de energía gastado<a name="tex2html14" href="#foot127"><sup>14</sup></a> y obtendras puntos de luz<a name="tex2html15" href="#foot128"><sup>15</sup></a>. Este trabajo reporta unos beneficios para el clan que dirige la
ciudad.

</p><p>

</p><h1><a name="SECTION0004000000000000000000">
3 La Galaxia</a>
</h1>

<p>
En el juego la acción se desarrolla en las ciudades, que a su vez
se ven movidas a merced de la decisión de los clanes que batallan
entre si para conseguir el mayor poder.

</p><p>

</p><h2><a name="SECTION0004100000000000000000">
3.1 Organización planetraria</a>
</h2>

<p>
En nuestro juego hay una serie de planetas que se pueden habitar.
Podemos viajar a ellos, vivir en sus ciudades y trabajar en ellas.

</p><p>

</p><h2><a name="SECTION0004200000000000000000">
3.2 Nosotros en el mundo</a>
</h2>

<p>
Además de andar correteando por ahí podemos mezclarnos en la política
de las ciudades. Podemos ser miembros de un clan, con lo que estaremos
atados en cierta manera a las decisiones que tome el clan, y de estas
decisiones podemos vernos beneficiados o perjudicados.

</p><p>

</p><h2><a name="SECTION0004300000000000000000">
3.3 Clanes</a>
</h2>

<p>
Bueno, una vez que somos Caballeros/Guerreros, unos seres respetados
y temidos en el universo, podemos tener aspiraciones gobernantes.
Si es así, estamos pensando en hacer un clan, un grupo de adeptos
de la fuerza que se unen en un objetivo común. 

</p><p>
En un clan hay dos tipos de integrantes:

</p><p>

</p><ul>
<li>El Lider, es el dirigente del clan. Tiene el poder de admitir a gente
nueva, de asignar lo que paga el clan por trabajar, de empezar nuevos
trabajos, de fundar una ciudad y de planificar ataques contra otros
clanes o contra ciudades. Pero dista mucho de ser un tirano, el lider
se elije por votación de los miembros del clan con derecho a voto<a name="tex2html16" href="#foot134"><sup>16</sup></a>.
</li>
<li>Los demás, los miembros del clan son tan importantes como el lider,
pues al fin y al cabo son ellos los que lo elígen. 
</li>
</ul>
Formar parte de un clan nos puede dar ciertas ventajas. Si el clan
tiene naves podremos usarlas y nuestros compañeros nos prestaran ayuda
y consejo. No vamos a entrar a especificar todo lo que te permite
hacer un clan pero si unas pinceladas:

<p>
</p><dl>
<dt><strong>Donar</strong></dt>
<dd>Cualquier miembro del clan puede donar dinero al clan.
</dd>
<dt><strong>Ciudad</strong></dt>
<dd>El clan puede fundar sus propias ciudades. Las ciudades son
todo un mundo, pero a modo de resumen podemos decir que va a hacer
falta protegerlas de los posibles ataques externos y nos va a permitir
construir naves en los astilleros. Además en una ciudad hay que marcar
leyes, fijar los precios de curación y los de los bares y por supuesto
fijar un impuesto.
</dd>
<dt><strong>Vehículos</strong></dt>
<dd>El clan puede construir distintos tipos de vehículos,
tanto para el transporte como para la guerra. Solo se pueden construír
si tienes una ciudad.
</dd>
<dt><strong>Dar Trabajo</strong></dt>
<dd>Una vez que el clan tiene una ciudad, puede dar trabajo
en las minas y en sus astilleros. Hace falta tener una ciudad.
</dd>
<dt><strong>Batalla</strong></dt>
<dd>Los clanes pueden ser aliados, enemigos o neutrales entre
si. Un clan puede atacar a otro o atacar a una ciudad para conquistarla.
</dd>
</dl>


